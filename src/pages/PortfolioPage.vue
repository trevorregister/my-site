<template>
    <v-container>
      <v-row>
        <v-col cols="12">
            <h1>Portfolio</h1>
            <p>Here are some of the projects I have worked on.</p>
        </v-col>
      </v-row>
      <v-row v-if="!projectCardExpanded">
          <v-col v-for="project in _.orderBy(projects, 'order')" :key="project.id" md="6">
              <portfolio-card 
                @expand-project="handleExpandProject(project)" 
                :project="project"
              />
          </v-col>
      </v-row>
        <v-row v-else>
            <v-col>
                <portfolio-card 
                  @click="collapseProject" 
                  :project="expandedProject"
                  :showFullCard="true"
                />
            </v-col>
        </v-row>
    </v-container>
  </template>
  
  <script setup>
  import projects from '@/projects.json'
  import _ from 'lodash'
  import PortfolioCard from '@/components/PortfolioCard.vue'
  import { ref } from 'vue'

  const expandedProject = ref({})
  const projectCardExpanded = ref(false)

  const handleExpandProject = (project) => {
      projectCardExpanded.value = true
      expandedProject.value = project
  }

  const collapseProject = () => {
      projectCardExpanded.value = false
  }
  </script>
  